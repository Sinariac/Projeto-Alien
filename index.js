function ripleyOpcao() {

  var ripleyOpcao = prompt("O que Ripley deve fazer? Escolha entre 1 e 2:");

  if (ripleyOpcao == 1) {
    location = "../ripley/ripleyfase1.html";
  } else if (ripleyOpcao == 2) {
    location = "../ripley/ripleyfase2nao.html";
  } else {
      alert ("Resposta inválida")
    }
}

